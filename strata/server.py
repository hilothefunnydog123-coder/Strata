"""``strata serve`` — the local web app.

Standard library only, bound to loopback by default. Routes:

    GET /                     the single-page interface
    GET /api/health           version, and which networks are loaded
    GET /api/ask?q=&k=        the full result as JSON
    GET /api/stream?q=&k=     the same, preceded by server-sent progress events
    GET /api/export?q=&format=md|bib|json

``/api/stream`` exists because a real question takes several seconds — a PubMed
search, then a fetch, then grading — and a spinner that says nothing for six
seconds reads as a hang. The stream reports each stage as it starts, so the wait
is legible.

Nothing is stored: no query log, no result cache beyond the shared PubMed cache
in :mod:`strata.cache`, no cookies. The server binds to 127.0.0.1 unless told
otherwise, and refuses to bind elsewhere without an explicit acknowledgement,
because this has no authentication and is not built to face a network.
"""
from __future__ import annotations

import json
import queue
import threading
import urllib.parse
from http.server import BaseHTTPRequestHandler, ThreadingHTTPServer

from . import __version__
from .query import ask
from .synthesize import key_finding
from .ui import PAGE

MAX_K = 20
MAX_QUESTION = 400


def _enrich(payload: dict) -> dict:
    """Add the display-ready fields the front end needs.

    Kept here rather than in :class:`strata.query.Evidence` so the library's own
    JSON stays a faithful description of the appraisal, without presentation
    concerns leaking into it.
    """
    for src, ev in zip(payload.get("sources", []), payload.get("_evidence", [])):
        article, g = ev.article, ev.grade
        src["journal"] = article.journal
        src["finding"] = key_finding(article.abstract)
        if g.effect is not None and g.effect.has_interval:
            src["effect_est"] = g.effect.estimate
            src["effect_ci"] = [g.effect.ci_low, g.effect.ci_high]
            src["effect_is_ratio"] = g.effect.is_ratio
    payload.pop("_evidence", None)
    return payload


def build_payload(result) -> dict:
    payload = result.as_dict()
    payload["_evidence"] = result.evidence
    return _enrich(payload)


